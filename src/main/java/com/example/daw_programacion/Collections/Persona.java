package com.example.daw_programacion.Collections;

public class Persona {
    String dni;
    String apelido;
    String nombres;
    String sexo;
    Integer edad;
    Double peso;

    public Persona(String dni, String apelido, String nombres, String sexo, Integer edad, Double peso) {
        this.dni = dni;
        this.apelido = apelido;
        this.nombres = nombres;
        this.sexo = sexo;
        this.edad = edad;
        this.peso = peso;
    }


}
